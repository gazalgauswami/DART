//Bitwise operator & | ~ ^ a<<b a>>b

main() {
  int a = 5;
  int b = 7;

  //128 64 32 16 8 4 2 1
  //0   0  0  0  0 1 0 1 = 5
  //0   0  0  0  0 1 1 1 = 7
  //0   0  0  0  0 1 0 1 = 5
  print(a & b);

  //128 64 32 16 8 4 2 1
  //0   0  0  0  0 1 0 1 = 5
  //0   0  0  0  0 1 1 1 = 7
  //0   0  0  0  0 1 1 1 = 7
  print(a | b);

  //128 64 32 16 8 4 2 1
  //0   0  0  0  0 1 0 1 = 5
  //0   0  0  0  0 1 1 1 = 7
  //0   0  0  0  0 0 1 0 = 2
  print(a ^ b);

  print(~a);

  print(a = a & b);

  print(a << b);

  print(a >> b);
}
