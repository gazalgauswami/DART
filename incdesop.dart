// Increment and decrement ++ --
//1.post type ( change the value next line)
//2.pre type ( Change the value same line )

main() {
  var a = 10;

  print(a);
  print(--a); //pre type descrement
  print(a++); // post type increment
  print(a);
  print(a--); //post type descrement
  print(a);
  print(++a); //pre type increment
}
